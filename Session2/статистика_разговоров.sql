select dbo.Телефоны_абонентов.номер_телефона,dbo.Абоненты.ФИО,count(dbo.Звонки.код_звонка)
from dbo.Абоненты INNER JOin
dbo.Телефоны_абонентов on dbo.Абоненты.Код_абонента = dbo.Телефоны_абонентов.Код_телефона inner join
dbo.Звонки on dbo.Звонки.код_звонка = dbo.Телефоны_абонентов.Код_телефона
GROUP BY dbo.Телефоны_абонентов.номер_телефона,dbo.Абоненты.ФИО,dbo.Звонки.код_звонка